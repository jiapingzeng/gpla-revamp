<script>
export default {
  name: "Fellowships",
  mounted() {
    // slide toggles
    const ucla_toggle = document.getElementById("ucla-slide-toggle");
    const caltech_toggle = document.getElementById("caltech-slide-toggle");
    const usc_toggle = document.getElementById("usc-slide-toggle");

    // fold/unfold: change width of slide between 0 and full (for sliding animation)
    const fold = id => document.getElementById(id).classList.add("folded");
    const unfold = id => document.getElementById(id).classList.remove("folded");

    // hide/show: toggle visibility and opacity (for slide content fade in/out)
    const hide = id => document.getElementById(id).classList.add("hidden");
    const show = id => document.getElementById(id).classList.remove("hidden");

    // sleep: helper function for syncing animation
    const sleep = duration => new Promise(r => setTimeout(r, duration));

    const toggle_slide = slide => {
      let slides = ["ucla-slide", "caltech-slide", "usc-slide"];
      return async () => {
        unfold(slide);
        show(slide);
        slides
          .filter(s => s !== slide)
          .forEach(s => {
            fold(s);
            hide(s + "-content");
          });
        await sleep(200);
        show(slide + "-content");
        await sleep(100);
        slides
          .filter(s => s !== slide)
          .forEach(s => {
            hide(s);
          });
      };
    };

    // default slide shown
    // making every slide folded+hidden then expanding with script seems to alleviate weird animation that would happen once otherwise
    toggle_slide("ucla-slide")();

    // show/hide on click
    ucla_toggle.onclick = toggle_slide("ucla-slide");
    caltech_toggle.onclick = toggle_slide("caltech-slide");
    usc_toggle.onclick = toggle_slide("usc-slide");

    // show/hide on hover
    // sometimes buggy, possibly due to async timing
    /*
    ucla_toggle.onmouseenter = show_ucla_slide;
    caltech_toggle.onmouseenter = show_caltech_slide;
    usc_toggle.onmouseenter = show_usc_slide;
    */
  }
};
</script>
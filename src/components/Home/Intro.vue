<script>
export default {
  name: "Intro",
  mounted() {
    // switch to next slide
    const next_slide = () => {
      // find active slide elements
      let active_slide = [...document.getElementsByClassName("active-slide")];
      active_slide.map((e) => {
        // hide current slide
        e.classList.remove("active-slide");
        // get slide number from class name
        let current_slide = Number(
          [...e.classList].filter((c) => c.includes("slide-"))[0].substring(6)
        );
        // calculate next slide (1, 2, 3)
        let next_slide = (current_slide % 3) + 1;
        // show next slide
        [
          ...document.getElementsByClassName(`slide-${next_slide}`),
        ].forEach((e) => e.classList.add("active-slide"));
      });
    };

    // change slide every 5 seconds
    setInterval(next_slide, 5000);
  },
};
</script>